
public class Funkcje_matematyczne {

	public static void main(String[] args) {
		double a = 9.0;
		int b = 3;
		double pierwiastek = Math.sqrt(a);
		double potêga = Math.pow(a, b);
		
		System.out.println("pierwiastek z "+a+" wynosi: "+pierwiastek);
		System.out.println("Liczba "+a+" podniesiona do potêgi "+b+" to "+potêga);
				

	}

}
