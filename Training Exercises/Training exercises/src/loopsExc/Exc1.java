package loopsExc;

public class Exc1 {

	public static void main(String[] args) {
		for (int x = 0; x < 8; x++) {
			System.out.println(x);
		}
		System.out.println("Gotowe for");
		
	 int i = 0;
	 while (i < 8) {
		 System.out.println(i);
		 i++;
	 }
	 
	 System.out.println("gotowe while");

	}
}

